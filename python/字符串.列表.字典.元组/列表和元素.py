a = ['a', 'b', 'c', 'd']
# 列表元素下标是从0开始的，-1的下标是从右到左的
print(a[0], a[-1])    #结果为 a d

# 去列表的第1个到第3个元素，但不包含第3个元素[1,3)
print(a[1:3])     #结果为 ['b', 'c']

# 冒号之前不写，默认是从左边的第0个元素开始[0,3)
print(a[:3])   #结果为 ['a', 'b', 'c']

# 设置了步长为-1，则表示是从右到左，
# 而第一个冒号后面没写，则表示是从右边的第0个元素开始
# 而第二个冒号后面没写，则表示从右到左的左边最后一个元素
print(a[::-1])    #结果为 ['d', 'c', 'b', 'a']

# 从左向右，步长为2
print(a[::2])     #结果为 ['a', 'c']

b = 'book'
