# 向列表中增加元素
e = [1,2,3,4]
e.append(500)     
print(e)         #结果为[1, 2, 3, 4, 500]

# 在1号位置之前插入元素100
e.insert(1,100)
print(e)         #结果为[1, 100, 2, 3, 4, 500]

# 在列表中移除删除指定索引位置的元素,默认是删除最后一个元素，并返回被删除的元素值
e1 = e.pop(1)
e1 = e.pop()
print(e1, e) 

# 在列表中移除指定元素    
e.remove(3)      
print(e)         #结果为[1, 2, 4, 500]

# 清空列表
e.clear()

# 列表排序
f = [1,100,2,3,56,98]
f.sort()              # 默认从小到大排序
print(f)
f.sort(reverse=True)  # 从大到小排序
print(f)

# 反序
g = ['a', 1, 3, 'd', 'b', 4]
g.reverse()
print(g)

# 将字符串转换成列表
h = 'hello world'
i = list(h)         #结果为 ['h', 'e', 'l', 'l', 'o', ' ', 'w', 'o', 'r', 'l', 'd']
print(i)       

# 将列表转换成字符串
print("".join(i))   #结果为hello world